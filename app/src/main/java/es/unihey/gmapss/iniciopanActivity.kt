class iniciopanActivity : AppCompatActivity() {
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        enableEdgeToEdge()
        setContentView(R.layout.activity_iniciopan)
        ViewCompat.setOnApplyWindowInsetsListener(findViewById(R.id.main)) { v, insets ->
            val systemBars = insets.getInsets(WindowInsetsCompat.Type.systemBars())
            v.setPadding(systemBars.left, systemBars.top, systemBars.right, systemBars.bottom)
            insets
        }



        val perd= findViewById<CardView>(R.id.perd)
        perd.setOnClickListener {
            val animation = AnimationUtils.loadAnimation(applicationContext, R.anim.scale_animation)
            perd.startAnimation(animation)
           
        }
    }
}
